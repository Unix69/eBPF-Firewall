class EventEmitter:
    """Simple internal event emitter to dispatch firewall events to registered listeners."""
    def __init__(self):
        self._listeners = []

    def add_listener(self, callback):
        self._listeners.append(callback)

    def event(self, event_obj):
        """Dispatches an event to all registered callbacks and logs it."""
        log_msg = f"[EVENT] {event_obj.format()}"
        if "FAILED" in event_obj.type.name or "DEAD" in event_obj.type.name:
            logger.error(log_msg)
        else:
            logger.info(log_msg)
        for listener in self._listeners:
            try:
                listener(event_obj)
            except Exception as e:
                logger.error(f"Error executing event listener: {e}")


class Exception(Exception):
    """Base application exception supporting severity layers and chaining causes."""
    class ExceptionSeverity(Enum):
        INFO = auto()
        WARNING = auto()
        ALERT = auto()
        CRITICAL = auto()

    def __init__(self, code, message, severity, cause=None):
        super().__init__(message)
        self.code = code
        self.message = message
        self.severity = severity
        self.cause = cause

    def format(self) -> str:
        base = f"[{self.severity.name}] Code={self.code.name if self.code else 'NONE'} - {self.message}"
        if self.cause:
            base += f" | Cause: {str(self.cause)}"
        return base


class RecoveryManager:
    """Manages mapping between specific exceptions and their respective recovery actions."""
    def __init__(self):
        self._registry = {}

    def register(self, exception_cls, action):
        self._registry[exception_cls] = action

    def recover(self, exc):
        """Finds and executes the registered recovery strategy for a given exception."""
        action = self._registry.get(type(exc))
        if action:
            action.execute(exc)
            return True
        return False

    def recoverable(self, notify=None, rethrow=False):
        """Decorator to automatically catch exceptions and route them through the recovery engine."""
        def decorator(func):
            @wraps(func)
            def wrapper(*args, **kwargs):
                try:
                    return func(*args, **kwargs)
                except Exception as e:
                    if notify:
                        notify(*args, e)
                    # Attempt automated resolution
                    recovered = self.recover(e)
                    if not recovered or rethrow:
                        raise e
            return wrapper
        return decorator

recovery_manager = RecoveryManager()

