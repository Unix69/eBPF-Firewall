#=============== Automated Recovery Actions ===============#

class RecoveryAction:
    """Interface outlining executable self-healing strategies for trapped faults."""
    def execute(self, exc: Exception):
        pass

class FirewallRecoveryAction(RecoveryAction):
    """Context aware recovery base ensuring direct execution access to the active Firewall instance."""
    def __init__(self, firewall: "Firewall"):
        self.fw = firewall

class FirewallUnloadedRecoveryAction(FirewallRecoveryAction):
    def execute(self, exc: FirewallUnloadedException):
        """Attempts to dynamically reload the kernel hook maps if the driver drops out."""
        try:
            logger.warning("Recovery: Firewall unloaded. Attempting reloading.")
            self.fw.load()
        except Exception as e:
            logger.error(f"Recovery failed: {e}")
            self.fw.event(FirewallEvent(
                type=FirewallEventType.RESTART_FAILED,
                message="Firewall recovery restart failed",
                interface=self.fw.interface,
                error=e
            ))

class FirewallRuleRecoveryAction(FirewallRecoveryAction):
    def execute(self, exc: FirewallRuleOperationException):
        """Inverts failed operations to roll back dirty rules inside kernel space maps."""
        try:
            logger.warning(f"Recovery: Rule operation failed ({exc.code}). Attempting rollback.")
            if exc.rule:
                if exc.code == FirewallExceptionCode.BLOCK_IP_FAILED:
                    self.fw.unblock_ip(exc.rule.get("ip"))
                elif exc.code == FirewallExceptionCode.UNBLOCK_IP_FAILED:
                    self.fw.block_ip(exc.rule.get("ip"))
                elif exc.code == FirewallExceptionCode.BLOCK_PORT_FAILED:
                    self.fw.unblock_port(exc.rule.get("port"), exc.rule.get("proto", "tcp"))
                elif exc.code == FirewallExceptionCode.UNBLOCK_PORT_FAILED:
                    self.fw.block_port(exc.rule.get("port"), exc.rule.get("proto", "tcp"))
        except Exception as e:
            logger.error(f"Recovery failed for rule rollback: {e}")
            self.fw.event(FirewallEvent(
                type=FirewallEventType.RESET_RULES_FAILED,
                message="Rule recovery rollback failed",
                interface=self.fw.interface,
                rule=exc.rule,
                error=e
            ))

class FirewallRoutineRecoveryAction(FirewallRecoveryAction):
    def execute(self, exc: FirewallRoutineOperationException):
        """Forces a hard system reset sequence if structural procedures break mid-execution."""
        try:
            logger.warning(f"Recovery: Routine operation '{exc.operation}' failed. Attempting restart.")
            self.fw.restart()
        except Exception as e:
            logger.error(f"Recovery failed for routine: {e}")
            self.fw.event(FirewallEvent(
                type=FirewallEventType.RESTART_FAILED,
                message=f"Routine recovery for {exc.operation} failed",
                interface=self.fw.interface,
                error=e
            ))

class InvalidIPRecoveryAction(FirewallRecoveryAction):
    def execute(self, exc: InvalidIPAddressException):
        """Signals administrator awareness logs since string data formatting cannot be guessed automatically."""
        logger.warning(f"Invalid IP provided: {exc.ip}. Manual administrative parsing required.")

class FirewallThreadRecoveryAction(FirewallRecoveryAction):
    def execute(self, exc: FirewallThreadException):
        """Respawns the passive background monitoring daemon loop if it encounters unexpected crashes."""
        try:
            logger.warning("Recovery: Firewall thread dead. Attempting to restart thread.")
            with self.fw._lifecycle_lock:
                if self.fw._thread is None or not self.fw._thread.is_alive():
                    self.fw._stop_event.clear()
                    self.fw._thread = threading.Thread(
                        target=self.fw.run,
                        name="FirewallThread",
                        daemon=True
                    )
                    self.fw._thread.start()

            self.fw.event(FirewallEvent(
                type=FirewallEventType.RESTARTED,
                message="Firewall thread successfully restarted",
                interface=self.fw.interface
            ))
            logger.info("Firewall thread restarted successfully.")
        except Exception as e:
            logger.error(f"Firewall thread recovery failed: {e}")
            self.fw.event(FirewallEvent(
                type=FirewallEventType.RESTART_FAILED,
                message="Failed to recover firewall thread",
                interface=self.fw.interface,
                error=e
            ))


