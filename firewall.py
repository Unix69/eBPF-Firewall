@dataclass
class Event:
    """Abstract structural base representation for systemic telemetry tracking."""
    type: Enum
    source: str
    message: str | None = None
    payload: dict | None = None
    error: Exception | None = None
    code: Enum | None = None

    def format(self) -> str:
        """Serializes the core event properties into a human-readable log string."""
        parts = [f"Source={self.source}", f"Type={self.type.name}"]
        if self.message:
            parts.append(f"Msg={self.message}")
        if self.code:
            parts.append(f"Code={self.code.name}")
        if self.error:
            parts.append(f"Error={str(self.error)}")
        return " | ".join(parts)


@dataclass
class FirewallEvent(Event):
    """Firewall specialized event including contextual network details like rules or interfaces."""
    interface: str | None = None
    rule: dict | None = None
    stats: dict | None = None

    def __init__(
        self,
        type: FirewallEventType,
        message: str | None = None,
        interface: str | None = None,
        rule: dict | None = None,
        stats: dict | None = None,
        error: Exception | None = None,
        code: Enum | None = None,
        payload: dict | None = None,
    ):
        super().__init__(
            type=type,
            source="firewall",
            message=message,
            payload=payload,
            error=error,
            code=code,
        )
        self.interface = interface
        self.rule = rule
        self.stats = stats

    def format(self) -> str:
        """Extensions to the standard event serialization to include explicit rule parameters."""
        base = super().format()
        extra = []
        if self.interface:
            extra.append(f"Interface={self.interface}")
        if self.rule:
            extra.append(f"Rule={self.rule}")
        if self.stats:
            extra.append(f"Stats={self.stats}")

        if extra:
            return base + " | " + " | ".join(extra)
        return base


