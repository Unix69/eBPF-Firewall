class FirewallException(Exception):
    """Polymorphic base exception handler wrapper for the eBPF Firewall component."""
    @staticmethod
    def format(exc: "FirewallException") -> str:
        """Appends technical runtime identifiers depending on what subclass exception is caught."""
        base = super(FirewallException, exc).format()
        extra = []

        if isinstance(exc, InvalidIPAddressException):
            extra.append(f"IP={exc.ip}")
        elif isinstance(exc, FirewallRuleOperationException):
            extra.append(f"Rule={exc.rule}")
        elif isinstance(exc, FirewallRoutineOperationException):
            extra.append(f"Operation={exc.operation}")

        if extra:
            base += " | " + " | ".join(extra)
        return base


